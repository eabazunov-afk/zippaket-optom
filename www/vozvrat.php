<h2>Право на отказ от товара</h2>
<p>При дистанционном способе продажи покупатель вправе отказаться от товара:</p>
<ul>
    <li>в любое время <b>до его передачи</b>;</li>
    <li>в течение <b>7 дней</b> после передачи товара.</li>
</ul>
<p>Если в момент доставки не была предоставлена в письменной форме информация о порядке и сроках возврата товара надлежащего качества, покупатель вправе отказаться от товара в течение 3 месяцев с момента передачи.</p>

<h2>Условия возврата</h2>
<ul>
    <li>сохранены товарный вид и потребительские свойства товара;</li>
    <li>имеется документ, подтверждающий факт и условия покупки.</li>
</ul>
<p>Не подлежит возврату товар надлежащего качества, имеющий индивидуально-определённые свойства (например, упаковка с нанесением логотипа покупателя), если он может быть использован исключительно этим покупателем.</p>

<h2>Возврат денежных средств</h2>
<p>При отказе от товара продавец возвращает стоимость товара (за вычетом расходов на доставку возвращаемого товара) в срок не более 10 дней с даты предъявления требования.</p>

