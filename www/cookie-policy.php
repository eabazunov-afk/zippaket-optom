<h2>Что такое cookie</h2>
<p>Cookie — небольшие текстовые файлы, которые сайт сохраняет в браузере посетителя. Они помогают сайту работать, запоминать настройки и собирать обезличенную статистику посещаемости.</p>

<h2>Какие cookie мы используем</h2>
<ul>
    <li><b>Необходимые</b> — обеспечивают работу сайта (сессия, корзина, защита форм). Не могут быть отключены.</li>
    <li><b>Аналитические</b> — Яндекс.Метрика; собирают обезличенную статистику для улучшения сайта.</li>
    <li><b>Маркетинговые</b> — используются для оценки эффективности рекламы (если включены).</li>
</ul>

<h2>Управление cookie</h2>
<p>При первом визите вы можете принять все cookie, отклонить или настроить их в баннере согласия. Также cookie можно удалить или ограничить в настройках вашего браузера. Отключение части cookie может повлиять на работу отдельных функций сайта.</p>

